package day05operators;

public class IncrementDecrement {

	public static void main(String[] args) {
//int data tipinde bir variable olusturun
		//bu variable in degerini carpma ve bolme kullanarak artirin ve azaltin.
		int num1=12;
	//Uzun yol

		num1=num1*3;//36
		System.out.println(num1);
		// kisa yol
	num1*=3;//36*3=108
	System.out.println(num1);
	
	//Uzun yol ile bolme yaparak decrement
	
	num1=num1/9;
	System.out.println(num1);//108/9=12
	//kisa yol
	num1/=2; //num1=6
	System.out.println(num1);
	
			
		
		
		

	}

}
