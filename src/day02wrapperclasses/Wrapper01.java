package day02wrapperclasses;

public class Wrapper01 {

	public static void main(String[] args) {
		
// int data tipinin maximum ve minimum degerlerini bulup ekrana yazdiriniz.
		// int in wrapper  classi==> Integer
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.MIN_VALUE);
		//Bir classin icindeki methodlara ulasmak icin class isminden sonra
		// nokta koy,listeden secim yap.
		
	// long data tipinin max ve min degerini bulup ekrana yaziniz.
		// Wrapper in manasi, cevreleyen,kusatan demek.Bayram sekerinin kagidi gibi.
		System.out.println(Long.MAX_VALUE);
		
		System.out.println(Long.MIN_VALUE);
	// tum primitivlerin max ve min degerlerini wrapper classlarini kullanarak 
		//ekrana yazin.
		
		
		
		
	}

}
